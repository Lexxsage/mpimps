void SPI_init(void)
{
	DDRB |= (1<<SPI_MOSI)|(1<<SPI_SCK)|(1<<SPI_SS);//|(0<<SPI_MISO);  //ножки SPI на выход
	PORTB |= (1<<SPI_MOSI)|(1<<SPI_SCK)|(1<<SPI_SS);//|(1<<SPI_MISO); //низкий уровень
	SPCR = (1<<SPE)|(0<<DORD)|(1<<MSTR)|(0<<CPOL)|(0<<CPHA)|(1<<SPR1)|(0<<SPR0);////включим шину, объ€вим ведущим, передача старшим битом вперед, SPI Mode 0,

}

//функц€ю, котора€ будет передавать команды в данный драйвер.
//ѕараметром будет передаваемый байт, а в теле функции запишем данный байт в регистр SPDR и затем стандартным способом данный байт в шину SPI передадим
void SPI_WriteByte(uint8_t data)
{
	SPDR = data;
	while(!(SPSR & (1<<SPIF)));//‘лаг прерывани€ от SPI, устанавливаетс€ в 1 по окончании передачи байта данных.
}

//ƒанна€ функци€ будет передавать байт не просто в шину, а в определЄнный регистр микросхемы. 
//¬ходными параметрами будут адрес регистра и байт данных
//сначала опустим ножку CS, чтобы выбрать нашу микросхему, затем передадим байт с адресом регистра, а затем байт данных, ну и в конце поднимем CS.
void Sendto7219(uint8_t rg, uint8_t dt)
{
	PORTB &= ~(1<<SPI_SS);//опускаем ножку SS дл€ выбора микросхемы, затем передаем байт с адресом регистра, затем байт данных
	SPI_WriteByte(rg);
	SPI_WriteByte(dt);
	PORTB |= (1<<SPI_SS);
}

